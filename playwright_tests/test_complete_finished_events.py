@pytest.mark.django_db(transaction=True)
class TestMemberDashboardHidesEndedEvents:
    """Scenario 5: member dashboard hides ended registered events."""

    def test_dashboard_hides_ended_registered_event(
        self, django_server, browser,
    ):
        from events.models import EventRegistration

        _clear_events()
        _ensure_tiers()
        _create_user("member@test.com", tier_slug="free")

        ended_event = _create_event(
            slug="dashboard-ended",
            title="Dashboard Ended",
            start_offset=timedelta(hours=-2),
            end_offset=timedelta(hours=-1),
        )
        future_event = _create_event(
            slug="dashboard-future",
            title="Dashboard Future",
            start_offset=timedelta(days=3),
            end_offset=None,
        )

        from accounts.models import User
        user = User.objects.get(email="member@test.com")
        EventRegistration.objects.create(user=user, event=ended_event)
        EventRegistration.objects.create(user=user, event=future_event)
        connection.close()

        _run_cron()

        context = _auth_context(browser, "member@test.com")
        page = context.new_page()
        page.goto(f"{django_server}/", wait_until="domcontentloaded")

        body_text = page.locator("body").inner_text()
        assert "Dashboard Future" in body_text
        assert "Dashboard Ended" not in body_text
        context.close()


@pytest.mark.django_db(transaction=True)
class TestRegistrationRejectedForEndedEvent:
    """Scenario 6: registration POST is rejected for an event whose start has passed."""

    def test_register_after_start_returns_409(self, django_server, browser):
        _clear_events()
        _ensure_tiers()
        _create_user("member@test.com", tier_slug="free")
        _create_event(
            slug="late-register",
            title="Late Register",
            start_offset=timedelta(minutes=-90),
            end_offset=timedelta(minutes=-30),
            required_level=0,
        )

        _run_cron()

        # POST /api/events/<slug>/register using urllib with session cookie.
        # Django CSRF middleware rejects tokens shorter than 32 chars with
        # 403 before our view runs, so use a 32-char token (mirrors
        # playwright_tests/test_event_time_display.py::_auth_context).
        session_key = _create_session_for_user("member@test.com")
        csrf_token = "a" * 32
        request = urllib.request.Request(
            f"{django_server}/api/events/late-register/register",
            data=b"",
            method="POST",
            headers={
                "Cookie": (
                    f"sessionid={session_key}; csrftoken={csrf_token}"
                ),
                "X-CSRFToken": csrf_token,
                "Referer": django_server,
                "Content-Type": "application/json",
            },
        )

        try:
            urllib.request.urlopen(request, timeout=5)
            pytest.fail("Expected HTTPError 409 but request succeeded")
        except urllib.error.HTTPError as exc:
            assert exc.code == 409
            body = json.loads(exc.read().decode("utf-8"))
            assert body.get("error") == "Event is not open for registration"

        # The detail page must not render the registration card or the
        # external Join card. Browse as the authed user.
        context = _auth_context(browser, "member@test.com")
        page = context.new_page()
        page.goto(
            f"{django_server}/events/late-register",
            wait_until="domcontentloaded",
        )
        assert page.locator(
            '[data-testid="event-registration-card"]'
        ).count() == 0
        assert page.locator(
            '[data-testid="event-external-join-card"]'
        ).count() == 0
        context.close()


