"""Playwright E2E for issue #573: cron flips ended events to completed.

Covers the 8 BDD scenarios in the issue:
1. Visitor sees a just-ended event move from upcoming to past on /events.
2. Visitor still sees a future event as upcoming.
3. Visitor sees an event without `end_datetime` treated as ended after 1h.
4. Visitor sees an event that just started still as upcoming.
5. Member dashboard hides ended registered events.
6. Registration POST is rejected for an event whose start has passed.
7. Cancelled event is not silently flipped to completed.
8. Public /events upcoming-section count drops after the cron runs.

The cron task is invoked directly in test setup via
`events.tasks.complete_finished_events.complete_finished_events`. We never
wait for Django-Q to tick.

Usage:
    uv run pytest playwright_tests/test_complete_finished_events.py -v
"""

