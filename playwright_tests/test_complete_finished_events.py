os.environ.setdefault("DJANGO_ALLOW_ASYNC_UNSAFE", "true")
from django.db import connection  # noqa: E402


def _clear_events():
    from events.models import Event, EventRegistration

    EventRegistration.objects.all().delete()
    Event.objects.all().delete()
    connection.close()


def _create_event(
    *,
    slug,
    title=None,
    status="upcoming",
    start_offset=None,
    end_offset=None,
    required_level=0,
):
    from events.models import Event

    if title is None:
        title = slug
    now = timezone.now()
    start_dt = now + (start_offset or timedelta(days=7))
    end_dt = None if end_offset is None else now + end_offset
    event = Event.objects.create(
        slug=slug,
        title=title,
        description="Auto-generated by tests.",
        start_datetime=start_dt,
        end_datetime=end_dt,
        status=status,
        required_level=required_level,
    )
    connection.close()
    return event


def _run_cron():
    from events.tasks.complete_finished_events import complete_finished_events

    flipped = complete_finished_events()
    connection.close()
    return flipped


def _refresh_status(slug):
    from events.models import Event

    status = Event.objects.get(slug=slug).status
    connection.close()
    return status


@pytest.mark.django_db(transaction=True)
class TestJustEndedEventMovesToPast:
    """Scenario 1: just-ended event moves from upcoming to past on /events."""

    def test_just_ended_event_moves_from_upcoming_to_past(
        self, django_server, page,
    ):
        _clear_events()
        _ensure_tiers()
        _create_event(
            slug="today-just-ended",
            title="Today Just Ended",
            start_offset=timedelta(minutes=-90),
            end_offset=timedelta(minutes=-30),
        )

        _run_cron()

        page.goto(
            f"{django_server}/events",
            wait_until="domcontentloaded",
        )

        past_section = page.locator('[data-testid="events-past-section"]')
        assert past_section.count() == 1
        assert "Today Just Ended" in past_section.inner_text()

        # The event must NOT appear in the upcoming section. Scope the
        # locator to the upcoming-section testid so we don't get false
        # positives from sibling text (filter tabs, headings, etc.).
        upcoming_section = page.locator(
            '[data-testid="events-upcoming-section"]'
        )
        assert upcoming_section.count() == 1
        assert "Today Just Ended" not in upcoming_section.inner_text()

        # Detail page shows the Completed badge.
        page.goto(
            f"{django_server}/events/today-just-ended",
            wait_until="domcontentloaded",
        )
        badges_row = page.locator("h1").first.locator(
            "xpath=preceding-sibling::div[1]"
        )
        assert "Completed" in badges_row.inner_text()
        assert "Upcoming" not in badges_row.inner_text()


