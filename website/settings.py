def _resolve_secret_key(*, debug, env=None):
    """Resolve Django's ``SECRET_KEY`` from the environment.

    Production (``debug=False``): the env var ``SECRET_KEY`` MUST be set
    to a non-empty value that is not the dev fallback. Otherwise we raise
    ``ImproperlyConfigured`` at import time so the deploy fails fast
    rather than silently signing cookies / JWTs with a known key.

    Development (``debug=True``): if ``SECRET_KEY`` is unset or empty,
    fall back to ``DEV_FALLBACK_SECRET_KEY`` and emit a
    ``RuntimeWarning`` so the developer notices. If the env var is set,
    use it (no warning).
    """
    env = os.environ if env is None else env
    value = env.get('SECRET_KEY', '').strip()
    if not value or value == DEV_FALLBACK_SECRET_KEY:
        if debug:
            warnings.warn(
                'SECRET_KEY is not set; using insecure dev-only default. '
                'Set SECRET_KEY in your .env file for any non-trivial '
                'work. Generate one with: python -c "from '
                'django.core.management.utils import '
                'get_random_secret_key; print(get_random_secret_key())"',
                RuntimeWarning,
                stacklevel=2,
            )
            return DEV_FALLBACK_SECRET_KEY
        raise ImproperlyConfigured(
            'SECRET_KEY environment variable must be set when DEBUG=False. '
            'In production this is injected from AWS Secrets Manager '
            '(ai-shipping-labs/django-secret-key). The in-tree dev '
            'fallback is rejected on purpose and must not be used in '
            'production.'
        )
    return value


