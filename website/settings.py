def _bool_env(name, *, default, env=None):
    """Parse a boolean env var.

    Truthy values (case-insensitive): ``1``, ``true``, ``yes``.
    Anything else (including ``0``, ``false``, ``no``, empty string) is
    falsy. Missing / unset uses ``default``.
    """
